package com;

public class Runner {
	public static void main(String[] args) {
		int[] a= {10,20,30};
		for(int i=a.length-1;i>=0;i--)
		{
			System.out.println(a[i]);
		}
		String[] c= {"mango","banana","cherry","watermelon"};
		for(int i=c.length-1;i>=0;i--)
		{
			System.out.println(c[i]);
		}
	}

}
